#!/usr/bin/python3                                                                                 
# Functions of basic math operations                                                                          

def add(a, b) -> int:
    return (a + b)

def sub(a, b) -> int:
    return (a - b)

def mul(a, b) -> int:
    return (a * b)

def div(a, b) -> int:
    return int(a / b)
